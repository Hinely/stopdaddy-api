                disconnectPlayer(plr)
            end
            return
        end

        for _, plr in ipairs(Players:GetPlayers()) do
            if plr ~= LP then
                connectPlayer(plr)
            end
        end

        connections["PlayerAdded"] = Players.PlayerAdded:Connect(function(plr)
            task.wait(0.15)
            connectPlayer(plr)
