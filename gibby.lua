--// ===================== UTILS =====================
local function teamAllowed(plr)
    if FFAEnabled then return true end
    return plr.Team and TargetTeams[plr.Team]
end

local function visible(part)
    local origin = Camera.CFrame.Position
    local direction = part.Position - origin

    local params = RaycastParams.new()
    params.FilterType = Enum.RaycastFilterType.Exclude
    params.FilterDescendantsInstances = {LP.Character, Camera}
    params.IgnoreWater = true

    local result = workspace:Raycast(origin, direction, params)
    return not result or result.Instance:IsDescendantOf(part.Parent)
end

--// ===================== TARGET FIND =====================
local function getClosest()
    if not SilentAimEnabled then return nil end

    local closest = nil
    local shortest = FOVRadius
    local centerPos = Vector2.new(Camera.ViewportSize.X/2, Camera.ViewportSize.Y/2)

    for _,plr in ipairs(Players:GetPlayers()) do
        if plr ~= LP and plr.Character and teamAllowed(plr) then
            local hum = plr.Character:FindFirstChildOfClass("Humanoid")
            local head = plr.Character:FindFirstChild("Head")

            if hum and hum.Health > 0 and head and visible(head) then
                local pos, onscreen = Camera:WorldToViewportPoint(head.Position)
                if onscreen then
                    local dist = (Vector2.new(pos.X, pos.Y) - centerPos).Magnitude
                    if dist < shortest then
                        shortest = dist
                        closest = head
                    end
                end
            end
        end
    end

    return closest
end

RunService.Heartbeat:Connect(function()
    ClosestHitPart = getClosest()
end)

