local oldNamecall
oldNamecall = hookmetamethod(game, "__namecall", newcclosure(function(self, ...)
    local method = getnamecallmethod()
    local args = {...}

    if method == "Raycast"
        and self == workspace
        and not checkcaller()
        and SilentAimEnabled
        and ClosestHitPart
    then
        local origin = args[1]
        args[2] = (ClosestHitPart.Position - origin)
        return oldNamecall(self, unpack(args))
    end

    return oldNamecall(self, ...)
end))

--// ===================== ESP (HIGHLIGHT ONLY) =====================
local ESPObjects = {}

local function shouldHighlight(plr)
    if not ESPEnabled then return false end
    if plr == LP then return false end
    if FFAEnabled then return true end
    if not plr.Team or not LP.Team then return true end
    return plr.Team ~= LP.Team
end

local function getTeamColor(plr)
    if plr.Team then
        return plr.Team.TeamColor.Color
    end
    return Color3.fromRGB(255,255,255)
end

local function removeESP(plr)
    if ESPObjects[plr] then
        ESPObjects[plr]:Destroy()
        ESPObjects[plr] = nil
    end
end

local function applyESP(plr)
    if plr == LP then return end

    removeESP(plr)

    local char = plr.Character
    if not char then return end

    local hum = char:FindFirstChildOfClass("Humanoid")
    if not hum or hum.Health <= 0 then return end

    local hl = Instance.new("Highlight")
    hl.DepthMode = Enum.HighlightDepthMode.AlwaysOnTop
    hl.FillTransparency = 0.7
    hl.OutlineTransparency = 0
    hl.Adornee = char
    hl.Parent = game:GetService("CoreGui")

    ESPObjects[plr] = hl

    local function update()
        if not char or not hum or hum.Health <= 0 then
            removeESP(plr)
            return
        end

        if shouldHighlight(plr) then
            local col = getTeamColor(plr)
            hl.FillColor = col
            hl.OutlineColor = col
            hl.Enabled = true
        else
            hl.Enabled = false
        end
    end

    -- auto updates
    hum.Died:Connect(function()
        removeESP(plr)
    end)

    plr:GetPropertyChangedSignal("Team"):Connect(update)
    LP:GetPropertyChangedSignal("Team"):Connect(update)

    update()
end

--// PLAYER CONNECTIONS
local function onPlayer(plr)
    plr.CharacterAdded:Connect(function()
        task.wait(0.3)
        applyESP(plr)
    end)

    plr.CharacterRemoving:Connect(function()
        removeESP(plr)
    end)

    if plr.Character then
        applyESP(plr)
    end
end

for _,plr in ipairs(Players:GetPlayers()) do
    onPlayer(plr)
end

Players.PlayerAdded:Connect(onPlayer)
Players.PlayerRemoving:Connect(removeESP)

